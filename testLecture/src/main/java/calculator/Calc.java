package calculator;

public class Calc {

    public Calc(){

    }

    public int add(int x, int y){
        return x + y;
    }

    public int subtract(int x, int y){
        return x - y;
    }

    public int square(int x, int y){
        return x * y;
    }

    public double multiply(double x, double y){
        return x * y;
    }
}
